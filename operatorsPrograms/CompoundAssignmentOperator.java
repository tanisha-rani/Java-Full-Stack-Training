package operatorsPrograms;

public class CompoundAssignmentOperator {
	public static void main(String [] args) {
		int a=9;
		a+= 3;
		System.out.println(a);
		
		a-= 2;
		System.out.println(a);
		
		a*= 10;
		System.out.println(a);
		
		a/= 3;
		System.out.println(a);
		
		a%= 3;
		System.out.println(a);
		
	}
}
