package decisionMakingStatements;

public class SwitchCase {
	public static void main(String [] args) {
		int a=2;
		switch (a) {
		case 1:{
			System.out.println("Hiii");
			break;
		}
		case 2:{
			System.out.println("Hello");
			break;
		}
		case 3:{
			System.out.println("bye");
			break;
		}
		}
	}
}
